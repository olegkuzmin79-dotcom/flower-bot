def calendar_onboarding_nudge(count: int) -> str:
    if count >= CALENDAR_IDEAL_DATES:
        return (
            "Годовой календарь собран. За 5 дней до каждой даты пришлю подборку "
            "и оплату в 1 клик — ты только подтверждаешь."
        )
    if count >= CALENDAR_TARGET_DATES:
        return (
            f"В календаре уже {count} даты — отлично. Можешь добавить ещё одну "
            f"(годовщина, 8 марта…) или остановиться."
        )
    need = CALENDAR_TARGET_DATES - count
    return (
        f"Добавь ещё минимум {need} даты за этот раз — так соберём годовой пакет спокойствия, "
        f"а не разовую покупку в панике."
    )
